import turtle 

kashyap = turtle.Turtle()

kashyap.color("blue", "cyan")

kashyap.begin_fill()

kashyap.forward(100)
kashyap.left(90)
kashyap.forward(100)
kashyap.left(90)
kashyap.forward(100)
kashyap.left(90)
kashyap.forward(100)

kashyap.penup()
kashyap.forward(100)
kashyap.pendown()

kashyap.forward(100)
kashyap.left(90)
kashyap.forward(100)
kashyap.left(90)
kashyap.forward(100)
kashyap.left(90)
kashyap.forward(100) 

kashyap.end_fill()

turtle.done()